package com.sample;


public class Type {

  private long tId;
  private String tName;
  private String tInfo;


  public long getTId() {
    return tId;
  }

  public void setTId(long tId) {
    this.tId = tId;
  }


  public String getTName() {
    return tName;
  }

  public void setTName(String tName) {
    this.tName = tName;
  }


  public String getTInfo() {
    return tInfo;
  }

  public void setTInfo(String tInfo) {
    this.tInfo = tInfo;
  }

}
