package com.sample;


public class Cart {

  private long cId;
  private long uId;
  private long pId;
  private double cCount;
  private long cNum;


  public long getCId() {
    return cId;
  }

  public void setCId(long cId) {
    this.cId = cId;
  }


  public long getUId() {
    return uId;
  }

  public void setUId(long uId) {
    this.uId = uId;
  }


  public long getPId() {
    return pId;
  }

  public void setPId(long pId) {
    this.pId = pId;
  }


  public double getCCount() {
    return cCount;
  }

  public void setCCount(double cCount) {
    this.cCount = cCount;
  }


  public long getCNum() {
    return cNum;
  }

  public void setCNum(long cNum) {
    this.cNum = cNum;
  }

}
