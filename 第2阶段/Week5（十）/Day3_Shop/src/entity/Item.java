package com.sample;


public class Item {

  private long iId;
  private String oId;
  private long pId;
  private double iCount;
  private long iNum;


  public long getIId() {
    return iId;
  }

  public void setIId(long iId) {
    this.iId = iId;
  }


  public String getOId() {
    return oId;
  }

  public void setOId(String oId) {
    this.oId = oId;
  }


  public long getPId() {
    return pId;
  }

  public void setPId(long pId) {
    this.pId = pId;
  }


  public double getICount() {
    return iCount;
  }

  public void setICount(double iCount) {
    this.iCount = iCount;
  }


  public long getINum() {
    return iNum;
  }

  public void setINum(long iNum) {
    this.iNum = iNum;
  }

}
